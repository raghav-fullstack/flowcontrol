public class IfLoop {
    public static void main(String[] args) {
       int i=10, j=20;
       if(i>10 && j==20)
           System.out.println("No is greater than 10");
       else if(i<10 && j==20)
           System.out.println("No is less than 10");
       else
           System.out.println("No is equal to 10");


    }
}
